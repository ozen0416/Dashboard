st.markdown("""
Dans un monde en constante évolution, les défis environnementaux et sociétaux se multiplient. L’étude des données devient une clé essentielle pour comprendre les transformations en cours et identifier les solutions adaptées. Ce dossier se concentre sur trois grands axes, chacun offrant une perspective cruciale pour mieux appréhender notre époque :

---

### *Analyse des tendances climatiques*  
Le changement climatique représente l’un des enjeux majeurs du 21ᵉ siècle. À travers l’étude des données historiques, nous visons à identifier les tendances climatiques, comprendre les causes des variations observées et démontrer les impacts concrets sur les écosystèmes. Cette analyse cherche à répondre à une question essentielle : comment et pourquoi le climat se transforme-t-il si rapidement ? En dévoilant ces tendances, nous mettons en lumière l’urgence d’agir face aux conséquences du réchauffement global.

---

### *Analyse de l'énergie et des ressources naturelles*  
Nos besoins croissants en énergie et en matières premières exercent une pression sans précédent sur les ressources naturelles de la planète. Cette section examine la consommation énergétique et l'exploitation des ressources, en s'interrogeant sur les raisons d’une telle frénésie. Pourquoi consommons-nous autant ? Quels sont les mécanismes économiques, sociaux ou culturels qui sous-tendent cette consommation excessive ?

---

### *Analyse des comportements de consommation*  
La société moderne est marquée par une tendance à la surconsommation, un phénomène qui n’est pas sans conséquence sur l’environnement et sur nos modes de vie. Cette partie s’attache à décrypter les comportements des consommateurs et à mettre en évidence les facteurs qui favorisent ces excès. De la publicité omniprésente aux normes sociales en passant par la recherche de confort, nous analysons les forces qui façonnent nos choix quotidiens et leurs impacts sur l’économie et l’environnement.

---

Ces trois axes permettent une approche globale, mêlant données climatiques, énergétiques et comportementales, pour fournir des pistes de réflexion et d’action face aux défis contemporains.
""")

